<?php
class Formule{

	public static function multiplier($n1, $n2){
		return $n1 * $n2;
	}

	public static function additioner($n1,$n2){
		return $n1 + $n2;
	}

	public static function diviser ($n1,$n2){
		return $n1 / $n2;
	}

	public static function soustraire ($n1,$n2) {
		return $n1 - $n2;
	}


}
